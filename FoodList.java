
package fastfoodinformationsystem;

public class FoodList extends MainFrame{
    
    //For Encapsulation
    private double priceHamburger;
    private double priceFrenchFries;
    private double priceChickenNuggets;
    private double priceHamSandwich;
    private double priceRamen;
    private double priceFootlongSandwich;
    
    public double getpriceHamburger(){ 
        return priceHamburger;
    }
    public void setpriceHamburger(double Hamburger){
        this.priceHamburger = Hamburger;
    }
    
    public double getpriceFrenchFries(){
        return priceFrenchFries;
    }
    public void setpriceFrenchFries(double FrenchFries){
        this.priceFrenchFries = FrenchFries;
    }
    
    public double getpriceChickenNuggets(){
        return priceChickenNuggets;
    }
    public void setpriceChickenNuggets(double ChickenNuggets){
        this.priceChickenNuggets = ChickenNuggets;
    }
    
    public double getpriceHamSandwich(){
        return priceHamSandwich;
    }
    public void setpriceHamSandwich(double HamSandwich){
        this.priceHamSandwich = HamSandwich;
    }
    
    public double getpriceRamen(){
        return priceRamen;
    }
    public void setpriceRamen(double Ramen){
        this.priceRamen = Ramen;
    }
    
    public double getpriceFootlongSandwich(){
        return priceFootlongSandwich;
    }
    public void setpriceFootlongSandwich(double FootlongSandwich){
        this.priceFootlongSandwich = FootlongSandwich;
    }
    
}
